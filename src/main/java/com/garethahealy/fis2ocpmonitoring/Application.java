package com.garethahealy.fis2ocpmonitoring;

import org.apache.camel.builder.RouteBuilder;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.context.annotation.ImportResource;

/**
 * A spring-boot application that includes a Camel route builder to setup the Camel routes
 */
@SpringBootApplication
@ImportResource({"classpath:spring/camel-context.xml"})
public class Application extends RouteBuilder {

    // must have a main method spring-boot can run
    public static void main(String[] args) {
        SpringApplication.run(Application.class, args);
    }

    @Override
    public void configure() throws Exception {
        // @formatter:off
        from("netty-http:http://0.0.0.0:8082/camel/hello")
            .choice()
                .when(header("name"))
                .transform(simple("Hello ${header.name} I am ${sysenv.HOSTNAME} how are you?"))
            .otherwise()
                .transform(constant("Add a name parameter to uri, eg ?name=foo"));
        // @formatter:on
    }
}
